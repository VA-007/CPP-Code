#include<iostream>
using namespace std;

int main()
{
 
	cout<<"The size of different datatypes are: \n";
	
	cout<<"Int: "<<sizeof(int)<<endl;
    cout<<"Char: "<<sizeof(char)<<endl;
    cout<<"Float: "<<sizeof(float)<<endl;
    cout<<"Double: "<<sizeof(double)<<endl;
    cout<<"Usigned Int: "<<sizeof(unsigned)<<endl;
	 
 	
	return 0;
}

